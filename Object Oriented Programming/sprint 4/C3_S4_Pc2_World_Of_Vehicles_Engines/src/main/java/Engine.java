public class Engine {
    String modelNo;
    float displacement;
    int maxPower;
    int maxRpm;
    int numberOfCylinders;

    public Engine(String modelNo, float displacement, int maxPower, int maxRpm, int numberOfCylinders) {
        this.modelNo = modelNo;
        this.displacement = displacement;
        this.maxPower = maxPower;
        this.maxRpm = maxRpm;
        this.numberOfCylinders = numberOfCylinders;
    }
}






